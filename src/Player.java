class Player {
    private String name;
    private int hp                                         = 50;

    public Player(String name) {
        this.name = name;
    }

    public String getName() {return name;}

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getHp() {
        return hp;
    }
}
                                                                                                                                                                        